    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        
        for(int i=0;i<tm.getRowCount();i++)
		tm.removeRow(0);
		double tot=0;
		int codePV=Integer.parseInt(cbxPV.getSelectedItem().toString());
		SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
		
		String sql="select r.*,p.prixVente from commande r,produit p where pointdeVente='"+codePV+"' and r.produit=p.codeProd and date between '"+sdf.format(dateDeb.getDate())+"' and '"+sdf.format(dateFin.getDate())+"'";
		Statement st = null;
        try {
            st = conn.createStatement();
        } catch (SQLException ex) {
            Logger.getLogger(VueRevenu.class.getName()).log(Level.SEVERE, null, ex);
        }
		ResultSet rs = null;
        try {
            rs = st.executeQuery(sql);
        } catch (SQLException ex) {
            Logger.getLogger(VueRevenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while(rs.next()){
                int qte = 0;
                try {
                    qte = rs.getInt(2);
                } catch (SQLException ex) {
                    Logger.getLogger(VueRevenu.class.getName()).log(Level.SEVERE, null, ex);
                }
                int codeP=rs.getInt(4);
                double prix=rs.getDouble(6);
                tm.addRow(new Object[]{codeP,qte,rs.getObject(3),(prix*qte)});
                tot+=(prix*qte);
            }
        } catch (SQLException ex) {
            Logger.getLogger(VueRevenu.class.getName()).log(Level.SEVERE, null, ex);
        }
		tfTot.setText(String.valueOf(tot));
        try {
            st.close();
        } catch (SQLException ex) {
            Logger.getLogger(VueRevenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(VueRevenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

